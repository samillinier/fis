// Helper function to get store name from store number
export const getStoreName = (store: string | number | null | undefined): string => {
  if (store === null || store === undefined) return 'Unknown'
  const storeNum = typeof store === 'string' ? parseInt(store, 10) : store
  if (isNaN(storeNum)) return String(store)
  return storeNames[storeNum] || String(store)
}

